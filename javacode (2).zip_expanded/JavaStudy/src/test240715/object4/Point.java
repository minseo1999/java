package test240715.object4;

public class Point {
	private int x;
	private int y;
	private final double pi = 3.141592653589793;
	public Point() {
		super();
	}
	public Point(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public double getPi() {
		return pi;
	}
	
	

}
